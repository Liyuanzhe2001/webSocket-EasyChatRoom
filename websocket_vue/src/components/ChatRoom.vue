<template>
  <div class="main_part">
    <div class="left_part">
      <el-scrollbar ref="chatWindow" class="chat_window">
        <div v-for="message in messages" :key="message">
          <div v-if="message.type === 'in'">
            <LeftBubble
                :content="message.content"
                max-width="400px"/>
          </div>
          <div v-else>
            <RightBubble
                :content="message.content"
                max-width="400px"/>
          </div>
        </div>
      </el-scrollbar>
      <el-input
          :disabled="this.selectedPerson === ''"
          class="submit_window"
          v-model="submitText"
          :rows="5"
          resize="none"
          type="textarea"
          placeholder="在这里输入消息"
      />
      <el-button
          class="btn"
          type="primary"
          @click="submit"
      >
        发送
      </el-button>
    </div>
    <div class="right_part">
      <div class="person_list_part">
        <el-scrollbar height="249px">
          <p
              v-for="person in personList"
              :key="person"
              class="scrollbar-demo-item"
              @click="chatWith(person)"
          >
            {{ person }}
          </p>
        </el-scrollbar>
      </div>
      <el-scrollbar class="system_inform_part">
        <div v-for="person in personList" :key="person">
          <p>您的好友&nbsp;{{ person }}&nbsp;已上线</p>
        </div>
        <!--        暂无系统消息-->
      </el-scrollbar>
    </div>
  </div>
</template>

<script>
import LeftBubble from "@/components/bubble/LeftBubble";
import RightBubble from "@/components/bubble/RightBubble";
import {Right} from "@element-plus/icons-vue";
import {getUsername} from "@/api/user";

export default {
  name: "ChatRoom",
  components: {Right, LeftBubble, RightBubble},
  mounted() {
    this.initUsername();
  },
  data() {
    return {
      username: '',
      submitText: '',
      selectedPerson: '',
      personList: [],
      messages: [],
      ws: '',
    }
  },
  methods: {
    initUsername() {
      getUsername()
          .then(res => {
            this.username = res;
            this.initWebSocket();
          })
    },
    initWebSocket() {
      var vue = this;
      //创建websocket
      if (window.WebSocket) {
        vue.ws = new WebSocket("ws://localhost:8088/chat");
      }
      vue.ws.onopen = function (evt) {

      }
      vue.ws.onmessage = function (evt) {
        //接收服务器推送的消息
        var res = JSON.parse(evt.data);
        //判断是系统消息还是推送给个人的消息
        if (res.isSystem) {
          //系统消息
          vue.personList = [];
          for (var name of res.message) {
            if (name !== vue.username) {
              vue.personList.push(name);
            }
          }
        } else {
          if (vue.selectedPerson === res.fromName) {
            //将数据追加到聊天区
            vue.messages.push({
              type: "in",
              content: res.message,
            })
          }
        }
      }
      vue.ws.onclose = function (evt) {

      }
    },
    chatWith(person) {
      // TODO 查询与该用户的聊天记录
      this.messages = [];

      // 滚动到最底端
      this.scrollToBottom();
      this.selectedPerson = person;
    },
    scrollToBottom() {
      this.$refs.chatWindow.setScrollTop(this.$refs.chatWindow.wrapRef.scrollHeight)
    },
    submit() {
      this.messages.push({
        type: 'out',
        content: this.submitText
      })
      this.$nextTick(() => {
        this.scrollToBottom()
      })
      //定义服务端需要的数据格式
      var message = {toName: this.selectedPerson, message: this.submitText};
      //将输入的数据发送给服务器
      this.ws.send(JSON.stringify(message));
      this.submitText = '';
    }
  }
}
</script>

